package sample;

public class Employee {
    public Employee(String id, String name, String country, String province, String city, String postal, String street) {
        this.id = id;
        this.name = name;
        this.country = country;
        this.province = province;
        this.city = city;
        this.postal = postal;
        this.street = street;
    }

    public String id;
    public String name;
    public String country;
    public String province;
    public String city;
    public String postal;
    public String street;
}
